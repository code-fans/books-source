//神族文字－ＳＴＬ版
#include<bits/stdc++.h>
using namespace std;

int main()
{
  freopen("dictionary.in","r",stdin);
  freopen("dictionary.out","w",stdout);
  char english[11],foreign[11];
  map<string,string>translate;//记录Foreign到English的映射
  while(true)//输入字典
  {
    char t;  //临时变量
    if((t=getchar())=='\n')  //判定是否输入了空行
      break;
    else     //输入english
    {
      english[0]=t;
      int i=1;
      while(true)
      {
        t=getchar();
        if(t==' ')
        {
          english[i]='\0';
          break;
        }
        else
          english[i++]=t;
      }
    }
    cin>>foreign;
    getchar();  //处理 输入foreign后的 回车符
    translate[foreign]=english;
  }
  char word[11];
  while(cin>>word)//开始翻译
  {
    if(translate[word]!="")
      cout<<translate[word]<<endl;
    else
      cout<<"eh"<<endl;
  }
  return 0;
}
